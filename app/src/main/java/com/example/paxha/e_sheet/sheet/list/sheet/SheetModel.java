package com.example.paxha.e_sheet.sheet.list.sheet;

public class SheetModel {

    private int id;
    private String name;

    SheetModel(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
